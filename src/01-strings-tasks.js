/**
 * Returns the string representation of rectangle with specified width and height
 * using pseudograhic chars
 *
 * @param {number} width
 * @param {number} height
 * @return {string}
 *
 * @example
 *
 *            '┌────┐\n'+
 *  (6,4) =>  '│    │\n'+
 *            '│    │\n'+
 *            '└────┘\n'
 *
 *  (2,2) =>  '┌┐\n'+
 *            '└┘\n'
 *
 *             '┌──────────┐\n'+
 *  (12,3) =>  '│          │\n'+
 *             '└──────────┘\n'
 *
 */
function getRectangleString(width, height) {
  const topBord = `┌${'─'.repeat(width - 2)}┐\n`;
  let middlePart = '';
  for (let i = 0; i < height - 2; i += 1) {
    middlePart += `│${' '.repeat(width - 2)}│\n`;
  }
  const bottomBord = `└${'─'.repeat(width - 2)}┘\n`;
  const result12 = topBord + middlePart + bottomBord;
  return result12;
}

/**
 * Encode specified string with ROT13 cipher
 * See details:  https://en.wikipedia.org/wiki/ROT13
 *
 * @param {string} str
 * @return {string}
 *
 * @example
 *
 *   'hello' => 'uryyb'
 *   'Why did the chicken cross the road?' => 'Jul qvq gur puvpxra pebff gur ebnq?'
 *   'Gb trg gb gur bgure fvqr!' => 'To get to the other side!'
 *   'ABCDEFGHIJKLMNOPQRSTUVWXYZabcdefghijklmnopqrstuvwxyz'
 *    => 'NOPQRSTUVWXYZABCDEFGHIJKLMnopqrstuvwxyzabcdefghijklm'
 *
 */
function encodeToRot13(str) {
  const lettersUp = 'ABCDEFGHIJKLMNOPQRSTUVWXYZ';
  const letters = 'abcdefghijklmnopqrstuvwxyz';

  return str
    .split('')
    .map((c) => {
      const indexUp = lettersUp.indexOf(c);
      if (indexUp >= 0) {
        if (indexUp > 12) {
          return lettersUp[indexUp - 13];
        }

        return lettersUp[indexUp + 13];
      }

      const index = letters.indexOf(c);
      if (index >= 0) {
        if (index > 12) {
          return letters[index - 13];
        }

        return letters[index + 13];
      }

      return c;
    })
    .join('');
}

/**
 * Returns true if the value is string; otherwise false.
 * @param {string} value
 * @return {boolean}
 *
 * @example
 *   isString() => false
 *   isString(null) => false
 *   isString([]) => false
 *   isString({}) => false
 *   isString('test') => true
 *   isString(new String('test')) => true
 */
